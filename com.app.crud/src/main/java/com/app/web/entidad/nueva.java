package com.app.web.entidad;

public class nueva {
	private int id;
	private String nombre;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getNombre() {
		return nombre;
	}
	public void setNombre(String nombre) {
		this.nombre = nombre;
	}
	public nueva(int id, String nombre) {
		super();
		this.id = id;
		this.nombre = nombre;
	}
	public nueva() {
		super();
	}
	
	

}
